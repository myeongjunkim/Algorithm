    T.HOUR,
    IFNULL(A.COUNT, 0) AS COUNT
FROM 
    HOUR_TABLE T
LEFT JOIN
    (
        SELECT HOUR(DATETIME) AS HOUR, COUNT(*) AS COUNT
        FROM ANIMAL_OUTS
